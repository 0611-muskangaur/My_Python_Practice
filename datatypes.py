# Numeric Types
integer  = 42                # int
float  = 3.14159             # float
complex  = 2 + 3j            # complex

# Sequence Types
string  = "Hello, World!"     # str
list  = [1, 2, 3, 4, 5]      # list
tuple  = (1, 2, 3, 4, 5)      # tuple

# Mapping Type
dictionary  = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}                                   # dict

# Set Types
set  = {1, 2, 3, 4, 5}       # set
frozenset  = frozenset([1, 2, 3, 4, 5])  # frozenset

# Boolean Type
boolean_true = True          # bool
boolean_false = False        # bool

# None Type
none  = None                  # NoneType

# Print the examples and their types
print("Numeric Types:")
print("Integer:", integer , "Type:", type(integer ))
print("Float:", float , "Type:", type(float ))
print("Complex:", complex , "Type:", type(complex ))
print()

